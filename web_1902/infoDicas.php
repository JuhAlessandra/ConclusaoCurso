            <h3>Incineração</h3>
        <p>Os resíduos sólidos são destinados fornalhas ou usinas onde são queimados com excesso de oxigênio, sendo assim os materiais à base de carbono são decompostos gerando resíduos de cinzas. Em resumo um incinerador transforma os resíduos em gases, cinzas e escória. Depois os gases são resfriados para evitar a recomposição das cadeias orgânicas tóxicas, logo em seguida serão tratados em lavadores antes de serem lançados à atmosfera através de chaminés. 
