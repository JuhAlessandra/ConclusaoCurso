public class Estatistica {
    private int idestatistica;
    private int usuarioid;
    private String dataestatistica;
    private String tiporesiduo;
    private int quantidaderesiduo;

    public int getIdestatistica() {
        return idestatistica;
    }

    public void setIdestatistica(int idestatistica) {
        this.idestatistica = idestatistica;
    }

    public int getUsuarioid() {
        return usuarioid;
    }

    public void setUsuarioid(int usuarioid) {
        this.usuarioid = usuarioid;
    }

    public String getDataestatistica() {
        return dataestatistica;
    }

    public void setDataestatistica(String dataestatistica) {
        this.dataestatistica = dataestatistica;
    }

    public String getTiporesiduo() {
        return tiporesiduo;
    }

    public void setTiporesiduo(String tiporesiduo) {
        this.tiporesiduo = tiporesiduo;
    }

    public int getQuantidaderesiduo() {
        return quantidaderesiduo;
    }

    public void setQuantidaderesiduo(int quantidaderesiduo) {
        this.quantidaderesiduo = quantidaderesiduo;
    }

    
}
